void MCU_InitClock(/*uint32_t sys_freq*/void)
{
	/*  Fast Internal Oscillator (FRC) имеет номинальную частоту 8 МГц, которую
	    можно поделить вплоть до 31 кГц */
    CLKDIVbits.RCDIV = 0;	// 8 MHz (divide-by-1)
	CLKDIVbits.DOZE = 0;	// 0 - 1:1// Делитель периферийной частоты
	CLKDIVbits.DOZEN = 0;	// Разрешение делителя периферийно частоты: 1 - вкл./0 - частота 1:1
	/*
	 * Источник тактирования 
	 * 1 = Fast RC Oscillator with Postscaler and PLL module
	 */
//	OSCCONbits.COSC = 0x01;
	/* Отключение неиспользуемых модулей на портах */
    CMCONbits.C1EN = 0;	// компаратор
    CMCONbits.C2EN = 0;	// компаратор
    PMCONbits.PMPEN = 0;	// параллельный порт
	RCFGCALbits.RTCEN = 0;	// RTCC модуль
	CVRCONbits.CVREN = 0;	// Опорное напряжение компаратора
}

